#include "philo.h"

long	ft_atol(const char *n)
{
	long	res;
	int		neg;

	neg = 1;
	while ((*n >= '\t' && *n <= '\r') || *n == ' ')
		n++;
	if ((*n == '+' || *n == '-') && (*(n + 1) >= '0' && *(n + 1) <= '9'))
		if (*(n++) == '-')
			neg = -1;
	res = 0;
	while (*n && (*n >= '0' && *n <= '9'))
		res = (res * 10) + (*(n++) - '0');
	return (res * neg);
}
